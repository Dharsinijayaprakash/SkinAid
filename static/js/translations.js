let currentLang = localStorage.getItem('skinaid_lang') || 'en';

function setLanguage(lang) {
  if (lang !== 'en' && lang !== 'ta') lang = 'en';
  currentLang = lang;
  localStorage.setItem('skinaid_lang', lang);
  
  // Set html lang attribute
  document.documentElement.lang = lang;

  // Translate all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  // Translate inputs with data-i18n-placeholder
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (translations[lang] && translations[lang][key]) {
      el.setAttribute('placeholder', translations[lang][key]);
    }
  });

  // Update switcher values
  document.querySelectorAll('.lang-switcher-select').forEach(el => {
    el.value = lang;
  });

  // Update active state class on lang buttons if used
  document.querySelectorAll('.lang-btn').forEach(btn => {
    if (btn.getAttribute('data-lang') === lang) {
      btn.classList.add('active');
    } else {
      btn.classList.remove('active');
    }
  });

  // Trigger custom event for other scripts to respond
  const event = new CustomEvent('languagechanged', { detail: { language: lang } });
  document.dispatchEvent(event);
}

function t(key) {
  return (translations[currentLang] && translations[currentLang][key]) || key;
}

// Auto-run on DOMContentLoaded
document.addEventListener('DOMContentLoaded', () => {
  setLanguage(currentLang);
});
